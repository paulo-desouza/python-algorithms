# How to traverse through a tree

# three common patterns:
# - preorder
#       visit the root node first, then recursively do a preorder traversal od the left subtree, 
#       followed by a traversal of the right subtree.
# example: reading a book. see trees-implemented2.py for the preorder method


# - inorder
#       recursively do an inorder traversal on the left subtree, visit the root node, and do a 
#       recursive inorder traversal of the right subtree.

# - postorder
#       recursively do a postorder traversal of the left subtree, then the right subtree, and finally the root. 

